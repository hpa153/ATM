    private void btnLichSuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLichSuActionPerformed
        int dongChon = jTableNguoiDung.getSelectedRow();
        String soTaiKhoan = "";
        boolean ketQua = false;
        Status trangThai = Status.KHONG_CO;
        
        if(dongChon >= 0){
            soTaiKhoan = "" + jTableNguoiDung.getValueAt(dongChon, 0);
        }
        
        List<SaoKe> lstGiaoDich = DataProvider.getGiaoDichBus().docGiaoDich(soTaiKhoan);
        
        if(!lstGiaoDich.isEmpty()){
            trangThai = Status.TAI_KHOAN;
        }
        
        if(dongChon == -1){
            trangThai = Status.TAT_CA;
        }
        
        switch(trangThai){
            case KHONG_CO:{
                JOptionPane.showMessageDialog(rootPane, "This account does not have any transactions!");
                break;
            }
            case TAI_KHOAN:
            case TAT_CA:{
            frmLichSuGiaoDich frmHistory = new frmLichSuGiaoDich();
            frmHistory.setStk(soTaiKhoan);
            frmHistory.setVisible(true);     
            }
        }
    }//GEN-LAST:event_btnLichSuActionPerformed

