    public static void hienThiDanhSachNguoiDung() {
        String tuKhoa = "";
        tuKhoa = txtTuKhoa.getText();
        
        String[] tieuDe = new String[]{"Số tài khoản", "Họ tên", "Mật khẩu", "Ngày sinh", "Giới tinh", "Điện thoại", "Email", "Địa chỉ", "Số dư", "Mã tài khoản"};
        
        NguoiDungBusiness nguoiDungBusiness = new NguoiDungBusiness();
        
        List<NguoiDung> lstNguoiDung = DataProvider.getNguoiDungBus().timKiem(tuKhoa);
        
        DefaultTableModel model = new DefaultTableModel(tieuDe, 0);
        
        Object row[];
        
        for(NguoiDung nd : lstNguoiDung){
            row = new Object[10];
            row[0] = nd.getSoTaiKhoan();
            row[1] = nd.getHoTen();
            row[2] = nd.getMatKhau();
            row[3] = nd.getNgaySinh();
            row[4] = nd.getGioiTinh();
            row[5] = nd.getDienThoai();
            row[6] = nd.getEmail();
            row[7] = nd.getDiaChi();
            row[8] = nd.getSoDu();
            row[9] = nd.getTaiKhoan();
            
            model.addRow(row);
        }
        
        jTableNguoiDung.setModel(model);
    }
    
