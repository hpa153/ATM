    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        
        boolean ketQua = false;
        
        Status trangThai = Status.TIEP_TUC;
        List<NguoiDung> lstSTK = DataProvider.getNguoiDungBus().layDanhSach();
        
        if(txtSoTaiKhoan.getText().length() != 10){
            trangThai = Status.TAI_KHOAN;
        }
        if(txtMatKhau.getText().length() != 4){
            trangThai = Status.MAT_KHAU;
        }
        
        if(txtSoTaiKhoan.getText().isEmpty() || txtHoTen.getText().isEmpty() || txtMatKhau.getText().isEmpty() ||
                txtDienThoai.getText().isEmpty() || txtEmail.getText().isEmpty() ||
                txtDiaChi.getText().isEmpty() || txtSoDu.getText().isEmpty()){
            trangThai = Status.THONG_TIN;
        }
        
        if(!rbtnNam.isSelected() && !rbtnNu.isSelected()){
                trangThai = Status.NUT;
        }  
        
        switch(trangThai){
            case THONG_TIN:{
                JOptionPane.showMessageDialog(rootPane, "Please fill in all information of the form!");
                break;
            }
            case NUT:{
                JOptionPane.showMessageDialog(rootPane, "Please select your gender!");
                break;
            }
            case TAI_KHOAN:{
                JOptionPane.showMessageDialog(rootPane, "Account number includes 10 characters! Please enter valid account number!");
                break;
            }
            case MAT_KHAU:{
                JOptionPane.showMessageDialog(rootPane, "Password includes 4 characters! Please enter valid password!");
                break;
            }
            case TIEP_TUC:{
                String taiKhoan = "";
                NguoiDung objND = new NguoiDung();
        
                objND.setSoTaiKhoan(txtSoTaiKhoan.getText());
                objND.setHoTen(txtHoTen.getText());
                objND.setMatKhau(txtMatKhau.getText());

                objND.setNgaySinh((Date) jDateChooserNgaySinh.getDate());

                if(rbtnNam.isSelected()){
                    objND.setGioiTinh("Nam");
                }else{
                    objND.setGioiTinh("Nữ");
                }

                objND.setDienThoai(txtDienThoai.getText());
                objND.setEmail(txtEmail.getText());
                objND.setDiaChi(txtDiaChi.getText());
                objND.setSoDu(Double.parseDouble(txtSoDu.getText()));
                TaiKhoan objTK = (TaiKhoan)cboTaiKhoan.getSelectedItem();
                
                if(objTK != null){
                    taiKhoan = objTK.getMaTaiKhoan();
                }
                
                objND.setTaiKhoan(taiKhoan);
                boolean thucHien = true;
                
                if(!stk.isEmpty()){
                    ketQua = DataProvider.getNguoiDungBus().suaNguoiDung(objND);
                }else{
                    for(int i = 0; i < lstSTK.size(); i++){
                        if(txtSoTaiKhoan.getText().equals(lstSTK.get(i).getSoTaiKhoan())){
                            thucHien = false;
                        }
                    }
                if(thucHien){
                    ketQua = DataProvider.getNguoiDungBus().themNguoiDung(objND);
                }else{
                    JOptionPane.showMessageDialog(rootPane, "Account number already exists! Please enter valid account number!");
                }
            }
        }
        
        if(ketQua){
                JOptionPane.showMessageDialog(rootPane, "Successfully updated user information");

                frmQuanLy.hienThiDanhSachNguoiDung();

                this.setVisible(false);
            }
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

