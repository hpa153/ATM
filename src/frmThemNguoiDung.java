    private void hienThiTaiKhoan(){
        TaiKhoanBusiness taiKhoan = new TaiKhoanBusiness();
        
        List<TaiKhoan> lstTaiKhoan = DataProvider.getTaiKhoanBus().layDanhSach();
        
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        
        for(TaiKhoan tk : lstTaiKhoan){
            model.addElement(tk);
        }
        
        cboTaiKhoan.setRenderer(new TaiKhoanRender());
        cboTaiKhoan.setModel(model);
    }
    private void hienThiThongTinND() {
        NguoiDung objND = DataProvider.getNguoiDungBus().layChiTietNguoiDung(stk);
        if(objND != null){
            txtSoTaiKhoan.setText(stk);
            txtSoTaiKhoan.setEnabled(false);
            
            txtHoTen.setText(objND.getHoTen());
            txtMatKhau.setText(objND.getMatKhau());
            
            jDateChooserNgaySinh.setDate(objND.getNgaySinh());
            
            if(objND.getGioiTinh().equals("Nam")){
                rbtnNam.setSelected(true);
            }else{
                rbtnNu.setSelected(true);
            }
            
            txtDienThoai.setText(objND.getDienThoai());
            txtEmail.setText(objND.getEmail());
            txtDiaChi.setText(objND.getDiaChi());
            txtSoDu.setText(Double.toString(objND.getSoDu()));
            
            TaiKhoanBusiness taiKhoanBusiness = new TaiKhoanBusiness();
            TaiKhoan objTK = taiKhoanBusiness.layChiTietTaiKhoan(objND.getTaiKhoan());
            if(objTK != null){
                cboTaiKhoan.getModel().setSelectedItem(objTK);
            }
            
        }
    }
