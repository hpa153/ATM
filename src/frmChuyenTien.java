    private void btnXacNhanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanActionPerformed
        NguoiDung objND = DataProvider.getNguoiDungBus().layChiTietNguoiDung(stk);
        frmSaoKe frmSK = new frmSaoKe();
        Double soDu = objND.getSoDu();
        Double soTien = Double.parseDouble(txtSoTien.getText());
        String noiDung = txtNoiDung.getText();
        
        boolean ketQua = false;
        
        if (soTien > soDu){
            JOptionPane.showMessageDialog(rootPane, "Số tiền cần chuyển lớn hơn số dư trong tài khoản!\n Không thể thực hiện giao dịch!");
            return;
        }
        if (noiDung.length() > 70){
            JOptionPane.showMessageDialog(rootPane, "Nội dung chuyển khoản đã vượt quá 70 ký tự!\n Vui lòng nhập lại!");
            return;
        }
        SaoKe objSaoKe = new SaoKe();
        objSaoKe.setSoTien(soTien);
        objSaoKe.setSoDu(soDu - soTien);
        objSaoKe.setNguoiThuHuong(txtHoTen.getText());
        objSaoKe.setSoTaiKhoanThuHuong(txtSTK.getText());
        objSaoKe.setNoiDung(txtNoiDung.getText());

        ketQua = DataProvider.getGiaoDichBus().chuyenTien(objSaoKe, stk);
        if (ketQua){
            frmSK.setSoTien(soTien);
            frmSK.setHoTen(txtHoTen.getText());
            frmSK.setSoTK(txtSTK.getText());
            frmSK.setNoiDung(txtNoiDung.getText());
            frmSK.setSoDu(soDu - soTien);
            frmSK.setVisible(true);
            this.setVisible(false);
            frmGiaoDich.hienThiChiTietNguoiDung();
        }
    }//GEN-LAST:event_btnXacNhanActionPerformed

