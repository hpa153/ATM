    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        this.setTitle("Chuyển tiền");
        NguoiDung objNguoiDung = DataProvider.getNguoiDungBus().layChiTietNguoiDung(stk);
        Double soDu = objNguoiDung.getSoDu();

        lblHoTen.setText(objNguoiDung.getHoTen());
        lblSoTaiKhoan.setText(objNguoiDung.getSoTaiKhoan());
        lblSoDu.setText("$" + soDu);
        txtNoiDung.getDocument().addDocumentListener(new DocumentListener() {
            @Override
            public void insertUpdate(DocumentEvent e) {
                demChieuDai(e);
            }

            @Override
            public void removeUpdate(DocumentEvent e) {
                demChieuDai(e);
            }

            @Override
            public void changedUpdate(DocumentEvent e) {
                demChieuDai(e);
            }
            
            private void demChieuDai(DocumentEvent documentEvent) {
                Document noiDung = documentEvent.getDocument();
                int dem = noiDung.getLength();
                lblCount.setText(dem + "/70");
            }
        });
    }//GEN-LAST:event_formWindowOpened

